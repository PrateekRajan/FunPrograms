package Programs;

public class FloatTest {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		float f1 = 3.2f;
		float f2 = 2.5f;
		if (f1 == 3.2) {
			System.out.println(f1 + " same");
		} else {
			System.out.println(f1 + " different");
		}
		if (f2 == 2.5) {
			System.out.println(f2 + " same");
		} else {
			System.out.println(f2 + " different");
		}
	}
}
